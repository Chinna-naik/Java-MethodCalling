package Methodcalling;

//Class definition
public class program {

	void start()
	{
		System.out.println("Program started"); // Prints when start() is called
		
	}
	void restarted()
	{
		System.out.println("Program restarted"); // Prints when restarted() is called
	}
	void stop()
	{
		System.out.println("Program stoped"); // Prints when stop() is called
	} 
} 
 