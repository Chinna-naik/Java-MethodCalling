package Methodcalling;

public class Human {

	String name = "Harish";
	
	void eat() // Method to eat 
	{
		System.out.println(name + " is a eating");
	}
	void sleep() // Method to sleep
	{
		System.out.println(name + " is a Sleeping");
	}
	void dancing() // Method to dancing
	{
		System.out.println(name + " is a Dancing");
	}
	void movie() // Method to movie
	{
		System.out.println(name + " is a watching movie");
	} 
}
